def evolve(inp, end):
	values = [int(x) for x in inp.split(",")]
	days = [0 for x in range(0, 9)]

	for v in values:
		days[v] += 1

	end_day = end

	for d in range(1, end_day+1):
		next = days[1:]
		next.append(days[0])
		next[6] += days[0]
		days = next
	print(d, sum(days), days)


def main_1(inp):
	evolve(inp, 80)


def main_2(inp):
	evolve(inp, 256)


def read_input(filename):
	with open(filename, "r") as f:
		raw = f.readlines()
	stream = map(lambda x: x.strip(" \n\t"), raw)
	return stream
	

